//Math objects are the inbuilt javascript Objects
val=Math.PI; //returns the PI value
console.log(val);

val=Math.round(2.9); //The Math.round() method rounds a number to the nearest integer. 
console.log(val);

val=Math.ceil(2.1); //The Math.ceil() method rounds a number rounded UP to the nearest integer.
console.log(val);

val=Math.floor(2.2); //The Math.floor() method rounds a number DOWN to the nearest integer.
console.log(val);

val=Math.sqrt(64); //The Math.sqrt() method returns the square root of a number.
console.log(val);

val=Math.abs(-3); //The Math.abs() method returns the absolute value of a number.
console.log(val);

val=Math.pow(1,4); //The Math.pow() method returns the value of x to the power of y (xy).
console.log(val);

val=Math.min(2,5,44,10); //The Math.min() method returns the number with the lowest value.
console.log(val);

val=Math.max(2,5,44,10); //The Math.max() method returns the number with the Highest value.
console.log(val);



